package h2Condition;

public class HelloSwitch {

    public static void main(String[] args) {
        int i = 1;
        switch (i) {
            case 0:
                System.out.println("zero");
                break;
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("default");
                break;
        }

    }

}
